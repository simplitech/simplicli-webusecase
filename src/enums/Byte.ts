/**
 * Represents a number in a byte unit
 */
export enum Byte {
  /**
   * Kilobytes (1024^1)
   */
  KILO = 1024 ** 1,

  /**
   * Megabytes (1024^2)
   */
  MEGA = 1024 ** 2,

  /**
   * Gigabytes (1024^3)
   */
  GIGA = 1024 ** 3,

  /**
   * Terabytes (1024^4)
   */
  TERA = 1024 ** 4,

  /**
   * Petabytes (1024^5)
   */
  PETA = 1024 ** 5,
}
